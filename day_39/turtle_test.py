import turtle

turtle.color('orange')
turtle.speed(1)
turtle.pensize(5)

while True:
    turtle.clear()
    turtle.forward(200.0)

    turtle.right(90.0)
    turtle.forward(100.0)

    turtle.right(90.0)
    turtle.forward(200.0)

    turtle.right(90.0)
    turtle.forward(100.0)

    turtle.right(90.0)